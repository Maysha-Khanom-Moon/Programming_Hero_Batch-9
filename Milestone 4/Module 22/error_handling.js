// js: left to right
// js follows PEMDAS (Parentheses, Exponents, Multiplication, Division, Addition, Subtraction)

// add: (concatenation)
console.log(2 + 2 + 'a'); // 4a
console.log('a' + 2 + 2); // a22
console.log(2 + 'a' + 2 + '\n'); // 2a2

// multiplication
console.log('a' * 2); // NaN
console.log(2 * 'a'); // NaN