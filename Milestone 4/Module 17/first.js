/**
 * ****** COMPARISON ******
 * 1. bigger: >
 * 2. less: <
 * 3. equal: ==
 * 4. greater than or equal: >=
 * 5. less then or equal: <=
 * 6. not equal: !=
 * 
 * -----------------------
 * 7. and: &&
 * 8. or: ||
 * 
 * -----------------------
 * 9. Logical not: !
 * --> !false: true
 * --> !true: false
 * 
 * 10. double not: !!
 * ==> !!false: false
 * ==> !!true: true
 */

console.log(5 > 10); // false
console.log(5 < 10); // true
console.log(5 == 10); // false
console.log(10 == 10); // true
console.log(15 >= 10); // true
console.log(15 <= 10); // false
console.log(10.1 == 10); // false
console.log(10.1 != 10); // true

