/**
 * -------------- Truthy --------------
 * 1. true
 * 2. any number(+ve, -ve) except 0
 * 3. any string
 * 4. empty object or object
 * 5. empty array or array
 * 
 * -------------- Falsy --------------
 * 1. false
 * 2. number = 0
 * 3. empty string = ''
 * 4. undefined
 * 5. null
 */


/**
 * ------------ type ------------
 * 1. primitive
 *      - number, string, boolean, null, undefined
 *      - same data type
 *      - work as copy --> value
 *      - fixed size
 * 
 * 2. non-primitive
 *      - array, object
 *      - various data type
 *      - work as reference --> address
 *      - dynamic size
 */


/**
 * ---- == vs === ----
 * 1. == check only value
 *      - 1 and true are equal
 *      - 1 and '1' are equal
 *      - 0 and false are equal
 * 
 * 2. === check value + type
 *      - 1 and true are not equal
 *      - 1 and '1' are not equal
 *      - 0 and false are not equal
 */
